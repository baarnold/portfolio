					<h2 class="mk-4">Physical Appearance</h2>
					<p>Saenrys Nightclaw has never shown her human form.  Many, including Saenrys herself, do not believe she has one anymore; if 
					she does, she hoesn't have the means, reason, or desire to access it.  Her worgen form is slightly shorter than average, covered 
					in dark fur with a grey-white undercoat.  Her face has a bit of mottling in coloration on her snout, and 
					her jade green eyes contrast nicely against the darker colors.  Her mane has a longer strand of hair braided over her shoulder, but 
					she otherwise does not attempt to pretty herself.  Her figure is built, bristling with the murderous musculature of a predator, and 
					wide hips common to female worgen.</p>
					<p>She wears armor common among the Kaldorei Sentinel Army, having been trained at Feathermoon Stronghold, she wears the ranking 
					rank symbol indicating herself as an Outrunner, and her uniform includes the insignia of the Moonclaw cadre - worgen 
					trained to serve alongside the Sentinels.  Her weapon of choice is a large Moonbow, common among the Sentinel 
					ranks, but she also maintains a long curved dagger as her sidearm, and, of course, her claws and fangs.  She has little shame in her 
					appearance, always ensuring the meticulous care of her uniform, but is otherwise not prudish at all, uncaring of how revealing 
					some of the Sentinel uniforms are.</p>
					<h2 class="mk-4">Character History</h2>
					<p>Saenrys Nightclaw was born Rys Heulen, the youngest of two daughters born to Sir Heulen of Silverpine, a landed Knight of Gilneas 
					who had earned his title from his father, and was a Veteran of the Second War.  Their family did not participate in the Northgate 
					Rebellion, having been locked out by the Greymane Wall.  During the Scourging of Lordaeron, the family relocated to Pyrewood, 
					rather than live on the outskirts, with Sir Heulen helping to maintain a vigilant guard on the populace.  During this time, the Heulen 
					daughters began to learn how to hunt in the wilds to keep the family and village fed, with Rys proving proficient with a bow.  
					As the Undead began to invade Silverpine, they would have overwhelmed Pyrewood had it not been for the 
					timely arrival of the tall, dark-skinned Elves who dispatched the Undead and collapsed the bridge leading to Southern Silverpine.  Inspired 
					by these bow-wielding warriors, Saenrys did her best to practice to be far better with her bow.</p>
					<p>During the Forsaken Invasion of Gilneas, Sir Heulen was slain and the population of Pyrewood were forced to evacuate or perish.  
					Rys and her older sister helped get their mother to Fenris Isle, where a displaced population of civilian refugees was growing 
					under the nominal protection of a Gilnean expatriate and his mercenary force.  Learning of the Worgen Curse, both Sisters Heulen partook 
					in the blood ritual performed by Lord Crowley at Fenris Keep, and they served in the Gilnean Liberation Front for some time, until the 
					truce between Sylvanas and Crowley was signed.  By that time, Rys had discovered that these dark-skinned elves of her youth were members 
					of the Alliance, and she set out to visit their capital at Darnassus.  She learned that the elves who had arrived at Pyrewood years before 
					had been chasing after one Illidan Stormrage, and their leader, Tyrande Whisperwind, had decided to save the village our of mercy when others, 
					such as Maeiv Shadowsong, had objected to their intervention.</p>
					<p>Upon learning that the Kaldorei were not only accepting the Gilneans as refugees, 
					but that Shandris Feathermoon was creating a cadre of Sentinel-trained Worgens in Feralas, Rys jumped at the chance and made her way to Feralas.  
					During her training, she was christened in the name of Elune, becoming a devout Elunite and adopting a new name: Nightclaw.  She 
					remains a devout Follower of Elune to this day, and answers to any member of the Kaldorei Ecclesiarchy or the Sentinel Army, intent on 
					being the embodiment of Elune's fury through the Blessing of Goldrinn.</p>
