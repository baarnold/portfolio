					<h2 class="mk-4">Physical Appearance</h2>
					<p>Alarek Renn is an every-man; an Average Joe at first glance, and often gets lost in the crowd.  He has short length brown hair, a short 
					trimmed beard, and green eyes.  There's nothing remarkable that stands out at first glance for the character, even the occasional facial 
					scar is not too eye catching, usually a cut here or there on the cheek or jaw.  He's over average size and build, though its obvious he's 
					fit enough to be a fighter.  He's usually seen in one of two moods: serious scowling, or jovial cheer.  The exception is during combat 
					situations, from sparring to battles-to-the-death, he's always wearing a wide, nearly maniacal smile, full of teeth, and the look in 
					the eyes of a psychopath.  A warrior through and through, he enjoys the thrill of combat more than any other form of entertainment.</p>
					<p>As with many Stromics, Alarek prides himself in martial prowess, almost always wearing a bronzed armor with black cloth covering 
					chainmail, and plates of metal and leather to provide protection and mobility.  He wears only an armored shoulder pauldron upon his right 
					shoulder, with deep cuts and gashes in the metal from a multitude of enemies.  His armor is kept meticulously cleaned and maintained, 
					though only the damages that compromise the protection are very repaired.  This causes his armor to be covered in scratches even despite 
					the polishing the armor receives from time to time.  The armor is just as scarred as the man underneath, who sports scars ranging from 
					stabbings, slashings, and shootings to burns, lightning, and impalements.  Its easy to tell that this man just refuses to die.  He is 
					always armed to the teeth, with an assortment of daggers and knives strapped to his torso and legs, with a one-handed sword sheathed 
					on his side and a large battle-ax as his primary weapon of choice.  And in a pinch, his helmet makes a lovely projectile.</p>
					<h2 class="mk-4">Character History</h2>
					<p>Alarek Renn was born amidst war, and has lived a life to reflect this.  Many Stromic civilians evacuated the kingdom after the 
					loss of the Tirasian Fleet meant that Stromgarde's nearly impenetrable defenses were circumvented by naval landings behind their 
					front lines.  Among these was Alanna Renn, whose husband was serving under Danath Trollbane's forces in Lordaeron, training alongside 
					their Alliance comrades to prepare a united military front while King Thoras Trollbane maintained only half the Stromic Army in 
					defending the Thandol Span to buy time for the Alliance.  Derek Renn was born near the Thoradin Wall during the evacuation, and was 
					raised in part with his maternal grandparents in Dalaran.  Once the war was complete, the family relocated to their ruined farmstead 
					in Stromgarde and began to rebuild.</p>
					<p>Alarek was trained from a young age by his father Adrian Renn and his uncle Derek "Wolfslaughter" to be a fighter, eventually 
					learning to harness his body itself as a weapon as a berserker.  He was given a Rite of Passage on his 14th Birthday to fight and 
					kill a local Witherbark Troll in single combat, returning with its head as proof.  He was self-taught to ride horses (primarily his 
					family's draft horse, Caramel) and eventually enlisted in the Stromic Army to join the cavalry.  The draft horse was deemed unsuitable 
					for combat duties, and instead Alarek was made a guard deputy, helping to make safe the roads in the Highlands for travellers while a 
					large number of Stromic Soldiers were sent off to serve in the Stromgarde Brigade to fight the Scourge occurring in nearby Lordaeron.</p>
					<p>Alarek's life would know strife after the assassination of King Thoras Trollbane and the invasion of Alteraci-based Syndicate forces 
					aligned with the Trolls and Ogres.  His patrols became far more treacherous as most of the Stromic army was either in Outland under 
					Danath Trollbane, or in Theramore under Jaina Proudmoore, leaving a small force behind that could barely hold the kingdom together.  
					In time, age and strife took Alarek's father, mother, and uncle and by the age of 21 he had no family left.  With the War in Icecrown 
					in full swing, Alarek's superiors recommended him to serve in the Alliance Military in the hopes that having Stromic soldiers aid 
					the Alliance, more aid would come to kick the Horde from the Highlands.  Alarek made his way to Stormwind and was instead deputized 
					to assist the Stormwind Guard patrol the frontiers of Stormwind, notably in Duskwood where he made his new home.</p>
					<p>Since then Alarek has served on a number of warfronts, including being given command of his own company of Stromic refugee soldiers, 
					taking the moniker Imperial Vanguard to hearken to the days of the Arathi Empire.  The Vanguard served in Pandaria, Blasted Lands, Draenor, 
					and the Broken Shore, where they fel with few survivors in a Last Stand during the initial invasion.  Alarek was brought to the attention 
					of Odyn, being given a seat in the Halls of Valor.  Death humbled the generally brash and headstrong Alarek, and with time and guidance of 
					the greatest warriors of Azeroth brought to the Halls of Valor in their own time, Alarek became far more observant and wizened in the 
					ways of combat; no longer was glory sought at the expense of others, and his shield and ax would be made to protect everyone, even his 
					comrades, rather than rushing off to defeat the enemy and leaving others undefended.</p>
					<p>Alarek was eventually granted a chance to return to the world of the Living to fight the Legion with the Valarjar.  Alarek, however, 
					did not view himself worthy of serving in the Valarjar after having gotten himself and his company slain, and desired to be returned 
					to Azeroth in a chance to redeem his company's loss.  While the Valarjar believed his death had been glorious, it was decided to let him 
					learn more about war and leadership by returning to Azeroth rather than remain with the Valarjar.  Alarek returned to Azeroth and 
					independently made his way to Argus, spending his days hunting down demons to aid to the war effort in a quest of self-discovery.  During 
					one hunt, he engaged with a Legion commander, merely to have the kill stolen from him by a strange elf.  A debate erupted about the kill, 
					as the elven huntress had tracked this prey for days before Alarek had brazenly attacked it.  The elf deftly turned the conversation on 
					Alarek, bringing up the ancient Stromic tradition of a life-debt, and stating that technically, he owed her said life-debt now.  Cornered 
					by his own traditions, Alarek resolved to travel with the huntress, who introduced herself as Faenrindis.  Even after Faenrindis partook 
					in the training to become a Void Elf, Alarek has maintained being her traveling companion, as the two have their own personal Arathi-Elven 
					concord.</p>
