					<h2 class="mk-4">Physical Appearance</h2>
					<p>Nyx is a Gilnean of dark complexion, due to her mixed lineage coming from sailors and shipwrights from the South Seas. 
					She's of diminutive stature, with a lean musculature and petite frame; her physical form favors agility over strength, and as such 
					she lacks in the department of endowment.  Instead, she makes up with her exotic complexion and her pert and fit figure, which goes 
					to show that smaller ladies can be alluring, too.  Her dark hair is grown out and drawn back in a lazy tail in the back, usually hidden 
					by her hood. Her eyes are a pale jade green, and contrast strongly against her dark skin.</p>
					<p>She often wears a suit of full leather and mail armor, all of it padded and darkened to avoid being noticed by sight or sound.  She  
					wears a heavy, dark leather hood to conceal herself from the elements, and has a large bandolier across her torso carrying her 
					ammunition.  Her primary weapon is a massive custom-made rifle: a large calibre, groove-bored construct that fires giant slugs  
					leaving few survivors.  Across her bandolier are individual shells, thicker than one's thumb and longer than one's palm.  The darkened 
					brass shells are designed for soft targets, while the occasional darkened steel shell indicate armor piercing rounds; large enough 
					to cause even warmachines to be appropriate targets.  Such a powerful rifle requires strength to carry and wield, and often the 
					diminutive sniper has to fire from a mounted or prone position to maintain accuracy and prevent recoil injuries to her arms and 
					shoulder.</p>
					<p>Since being commissioned as a Lieutenant for the Gilnean arm of the Alliance Military, she can also be found wearing a Gilnean 
					uniform on occassion, with a fur-lined hood and cloak to fight off the elements.  This uniform is only mildly modified from her older 
					attire, and as such she will often use her uniform as her new armor when needed.</p>
					<h2 class="mk-4">Character History</h2>
					<p>Born as Kyran Forsythe, she adopted the name "Nyx" after the Northgate Rebellion, where she served as a sniper for the Rebels.  
					Her family had moved to Keel Harbor and suffered greatly at the isolationist policies of post-Second War Gilneas, and when rebellion was 
					sparked, they joined Crowley's revolt.  An avid hunter growing up, she put her rifle to use targeting officers, couriers, and scouts 
					of the Royalist faction, earning her the distinction of being personally hunted near the close of the Rebellion by none other 
					than Alysdaire Dunraven himself.  She was eventually cornered and found, but it was not Alysdaire or the Royalists who had done her in - 
					she was already bleeding profusely from an abdominal wound. A Rebel traitor who identified himself as her fiance stood 
					holding the knife, and attempted to bargain for his freedom by trading her life.  Alysdaire instead shot the man cold, and tended to Nyx's 
					wounds.  When questioned why, he responded "Loyalty to any cause is better than treason to your loved ones."  Nyx was eventually pardoned 
					with many other Rebels and allowed to rejoin society.  She took up with Alysdaire's post-Rebellion Blackdawn organization, and became a 
					founding member.</p>
					<p>Nyx remained behind in Gilneas with Ander Carmyne, assisting in the Evacuation of Gilneas, though she remained unafflicted from the 
					Worgen Curse.  She joined up with the Gilneas Liberation Front and was one of the first to rejoin with Alysdaire at Fenris, providing 
					sniper support for the evacuation of civilians to the island keep.  She was eventually made the commander of Blackdawn's military forces, 
					primarily helping to strategize from her sniper posts during black operations.  In time, she acted as de facto second-in-command, 
					with Thaldred Kane in charge of the military operations.  By the time of the Broken Shore, Nyx had returned to military patrols and 
					overwatch, becoming Thaldred's second-officer instead of resuming her original post, more comfortable in the field than behind a desk.</p>
					<p>Since the fall of Darnassus, Nyx has been cashiered into Blackdawn's military component and commissioned as a Lieutenant in the Army 
					of the Alliance, serving directly under Alysdaire in a more administrative role than she is used to.</p>
