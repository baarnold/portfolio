          <h2 class="my-4">About &lt;Blackdawn&gt;</h2>
          <p>The Blackdawn guild has existed since March 2010, when it was created by Alysdair to play with the idea of a guild of 
		  black operatives  - both assassins and mercenaries - working for the Alliance with their only loyalties to themselves, 
		  lead by a Gilnean expatriate.  Before the guild could take off, Blizzard Entertainment announced Cataclysm and Worgen in October 
		  2010, and with the upcoming inclusion of Gilneas, the guild would change to have a Gilnean flair to it.</p>
		  
          <p>Blackdawn was a Cataclysm launch guild that already had membership of various races, which had a large spike of 
		  Worgen membership on day one.  Based in Fenris Isle, Blackdawn provided RP-PvP activities against cooperative Horde 
		  guilds in the Lordaeron area, and worked with Alliance guilds across the globe in RP-PvP, particularly open battles or 
		  black operations.  It was so successful that a number of other Gilnean- or Worgen-themed guilds were merged into Blackdawn 
		  to give it a more robust and diversified portfolio.</p>
        </div>
		<div class="col-lg-12">
		  <p>The guild went into hiatus during late Mists of Pandaria, in large part due to the long delay between content patches 
		  and the following expansion, Warlords of Draenor.  Due to dwindling population of the original server, Blackdawn's leading 
		  officers did not attempt to reopen the guild to new membership, content with the smaller and more personal feel of the guild 
		  and the lack of unecessary drama or problems larger guilds suffered.</p>
		  
		  <p>By the time of Legion's approach, the original server Blackdawn inhabitted had only three Alliance RP guilds of any 
		  noticeable size or activity, and server membership had dipped to an all-time low.  Wanting to revitalize the guild for 
		  Legion and future expansions, the guild had a fully democratic vote that was unanimous to relocate to Wrymrest Accord.  
		  Since February 2016, Blackdawn has resided on Wyrmrest Accord, reorganizing and getting accustomed to our new home.</p>
		  
		  <p>With Battle for Azeroth on the ground, Blackdawn looks to break out and become the active powerhouse it once had been, 
		  with a focus on quality over quantity, and preference over a smaller guild with many active and engaging members over a 
		  larger one with many missing roster names.</p>
		  
		  <p>Headed by Alysdaire as Guild Leader, Rosemary, Kane, and Carmyne as Officers, and boasting a small cadre of experienced, long-time 
		  Blackdawn members, we seek out not only new members but also new guilds with which to have cordial relationships, including 
		  allies and nemeses.  Our primary focus since our inception has, and forever will be: Fun, Friendships, and Fantasty First.</p>
