					<h2 class="mk-4">Physical Appearance</h2>
					<p>Alakthul is not as pointy and spikey as many expect demon hunters to be.  In due part to his training under Tharion Netherbane, he was disciplined 
					into never giving into the corruption of the fel, and has thus prevented the gradual visual signs of that corruption - he bears no horns, no hooves, 
					no scales, and no spikes.  His ashen dark skin is only recently beginning to shown signs of green fel corruption, and his hair has started to darken only 
					recently.  He has no eyes whatsoever, with scars running across his right eye in particular from some injury prior to his blinding ritual.  His dark hair 
					is kept back in a well-groomed braid, some time carrying flowers or ribbons weaved into it, not that he can see them.  His eyes are covered by thick 
					bandage wraps most of the time, concealing the empty sockets and the scarring around them.  His torso is riddled with scars of a long life in combat, 
					many from before his time as a demon hunter.  Purple runes designed to bind and contain the demon with arch about his chest, shoulders, and upper arms.</p>
					<p>Alakthul favors a more natural appearance where he can, generally the inclusion of bones or branches or horns, heralding to his days of being a Druid.  
					He prefers purple as a color, and with the assistance of others has assembled a modest outfit of purples, greys, and trimmed in gold.  Never having been 
					a fan of the kilt craze for demon hunters, he wears armored leggings made of cloth and hardened leather to grant him mobility, with large shoulder 
					pauldrons to absorb hits to the torso and arms.  His glaives are likewise purple in coloration, burning with an arcane magic.  He also wears a heavy 
					leather scarf and collar about his neck, concealing his lower face at most times.</p>
					<h2 class="mk-4">Character History</h2>
					<p>Alakthul was still young when the world was Sundered; his first memory being the terrifying quakes and tremors as the world was torn apart, and the skies 
					raging with chaos.  His father, a blacksmith, had been slain during the War of the Ancients providing his talents for Blackrook Hold, leaving his mother 
					to raise himself and his sister.  Seven hundred years later his mother became broken after capture and torture from satyrs during the War of the Satyr, 
					and his sister left to join the Sentinels in a vengeful rage.  Alakthul cared for his mother by himself in their home outside the ruins of Morlos'Aran for 
					some decades more until she finally one day disappeared without a trace.  Subsequent searches turned up no leads, and she was presumed to have wandered away 
					and gotten lost or killed in a state of delirium.  Alakthul, now able to start his own life's journey without worry, left to join the Cenarion Circle, 
					becoming a Druid of the Talon.</p>
					<p>Alakthul's life was generally uneventful until the War of the Shifting Sands.  Alakthul was not in hibernation at the Awakening of Ahn'Qiraj, and was a part 
					of Archdruid Fandral Staghelm reinforcements to aid his son Valstann after their plan to return life to the Silithid desert roused the Qiraji from their 
					age-long slumber.  Alakthul served as a scout and courier for the most part, often shifting into a raven to avoid being easily caught by the Qiraji silithid.  
					During one battle he took up the spear of a wounded Dryad and fought off the attacking silithid long enough for assistance to arrive.  Both he and the dryad 
					were mended to by a Keeper of the Grove, who thanked him for risking his life by granting him the Dryad spear he had used, with selfsame Dryad's blessing.  
					Throughout the rest of the war, he wielded the spear proudly in combat, with the exception being late in the war where he joined his brothers in flight to 
					assist the Bronze Dragonflight.</p>
					<p>Alakthul would not return to hibernation after the Shifting Sands, uneasy at the prospect of other such threats lurking in the hidden parts of the world.  
					He would continue to serve an active role as a guardian for Cenarion posts and enclaves, as well as a messenger across northern Kalimdor's settlements.  It 
					was not until the invasion of the Humans, Orcs, and Legion-led Scourge did he rejoin in battle again.  In particular he served under Illidan Stormrage's 
					command in the defense of what would become known as Felwood.  It was during the battle against Tichondrius's forces that Alakthul became convinced that 
					Illidan's claim of fighting fire with fire held more accuracy than his contemporaries gave him credit for.  Alakthul would remain in the area after Illidan's 
					banishment, desperate to purge the corruption from his home.  His sister would later die in the Battle of Mount Hyjal later in the war.</p>
