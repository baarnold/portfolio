					<h2 class="mk-4">Physical Appearance</h2>
					<p>Rafaelle Lenoir is a man who has always believed in the strength of the mind and the body, choosing a career path that has 
					elevated both traits. He’s not filled to the brim with muscle, but he keeps his form lithe. His face is angular, his skin lightly 
					tanned and his hair crisp and golden. No one has really seen him without facial hair, his whiskers giving him a dignified look. 
					His eyes are strikingly blue, seeming to almost illuminate even in low light. From his ears hang two pieces of fine jewelry--violet 
					crystals that pulse with energy. He is almost always on duty, and thus is always wearing one of his many uniforms. He prefers outfits 
					that are violet or blue, and he never wears robes given his status as a warmage. One outfit he’s taken to lately is a navy overcoat 
					(similar to 17th-18th century admiral coats) with Kirin Tor shoulder boards and rune-inscribed gloves. The buttons and trimming are 
					made of gold silk. Rafaelle always wears a leather strap around his torso to sheath his staff, an intricate gold staff with violet 
					ribbons and icy blue focus crystal. A flask of mana water always hangs on the staff as an emergency ration should the warmage need 
					the extra magic. </p>
					<h2 class="mk-4">Character History</h2>
					<p>Rafaelle was a court mage of Alterac's Perenolde family during the Second War. Young and charming, the Alteraci man always enjoyed 
					playing the "game" amongst the nobles, as he called it--gathering information and listening in on delicious rumours; perhaps witnessing 
					the occasional assassination. It was all so riveting to him. Rafaelle actually learned of Aiden Perenolde's betrayal in a similar 
					fashion, but before he could clear his own name from involvement, the Grand Alliance marched upon Alterac, taking him amongst their 
					prisoners. As a part of the Kirin Tor at the time, Rafaelle was labeled a traitor and placed into the Violet Hold.</p>
					<p>Many years passed in the hold until the coming of the Nexus Wars. Rafaelle was released from his prison in the Blue Dragonflight's 
					attack on Dalaran. However, when Rafaelle made no attempts to actually escape the Violet Hold in the coming chaos, his record was 
					reviewed by the council and he was released on a condition of good behavior. From there, Rafaelle regained his bearings, entering 
					the Kirin Tor army as a warmage to fight against the Blue Dragonflight and the Scourge.</p>
