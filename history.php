				<div class="card-body">
				  <p>Due to the unsanctioned assault on an Alliance Keep, those who participated were reprimanded, including Alysdaire Blackdawn, who had his rank suspended for one year at least, to be 
				  reinstated when requested and reviewed by the Grand Marshals Corran Ravencrest and Acele Walkinson.  Blackdawn at that time no longer had the authority or support of the Alliance High 
				  Command to operate as a military entity, and had broken the trust of a number of Gilnean patriots they had been aligned with for so long for their solo operations.  Hamstrung when it 
				  came to combat operations, the Blackdawn Organization was listless until the intervention of Alysdaire's newlywed wife, Reigan Ashford.  With her connections to Vice-Admiral Morgan 
				  Ashford, and her own humanitarian ideals, the organization took on new life by providing humanitarian assistance, including supplies, medical and food aid, evacuations, and even para-
				  military defense for Alliance outposts against external threats.  The Blackdawn Pathfinders, headed by Nyx Forsythe and Thaldred Kane, became an instrumental specialist mercenary force 
				  that could provide security or patrol details for various forces and organizations.  It was not until the Pandaria Campaign that Alysdaire would regain his military commission, being 
				  attached to the SI:7 mission for the White Rook as a member of a multi-factional force serving under Admiral Taylor.</p>
				  <p>Alysdaire served the stranded Alliance forces as a logistician, helping to maintain supply lines and scrounge up supplies to last them until reinforcements arrived.  Blackdawn arrived 
				  in the second wave that landed at Krasarang, and essentially they continued to serve under supervision from Commander Taldrus Dawnfield, per commands from Grand Marshal Acele Walkinson to 
				  keep an eye on their work.  Blackdawn also assisted Commander Dawnfield's joint assault up the Dustwallow Marsh to the Barrens to set up a foothold to overthrow Garrosh Hellscream when the 
				  Darkspear Rebellion began.  Due to Blackdawn's merchant work to fund their operations, they were offered Alcaz Isle to use as a supply port for Alliance and Horde efforts, building up a 
				  small yet fortified port, providing safe harborage for a Thalassian Fleet aligned with Vice Admiral Morgan Ashford's Alliance Fleet.  After Garosh's imprisonment, Blackdawn would remain on 
				  Alcaz for nearly a year, as they did not participate in the War on Draenor.  During this time, Blackdawn would suffer the loss of its humanitarian matron, as Reigan Ashford died from 
				  complications of child birth.  Since then, Alysdaire has taken steps to maintain the humanitarian focus of the organization, the memory of his late wife haunting him on many of his decisions.</p>
				  <p>Following a soul-searching sojourn by Alysdaire, Blackdawn were mysteriously ordered to make numerous preparations for what seemed to be an upcoming crisis.  With the assistance of 
				  Ander Carmyne's pirating, Morgan Ashford's assistance, and Taldrus Dawnfield (now a Marshal) forcing the Phantom Legion to surrender their larger weapons of war, Blackdawn was able to build a 
				  small fleet of four vessels: The Merchant Ship <em>Crownless</em>, The Pirate Frigate <em>Anderinlia</em>, the Medical Support Ship <em>Mercy</em> and the former Phantom Legion armored destroyer 
				  <em>VanCleef</em>, more appropriately renamed to <em>Silverlaine</em> under Blackdawn's banner.  In time, it was revealed that Alysdaire had seen terrible nightmares of sinking to the bottom 
				  of the sea amidst green flames, debris, and bodies while he had slept at the Shrine of Goldrinn during his sojourn.  Taking it as a sign from the Goddess Elune, Alysdaire had begun to prepare 
				  for what he believed was an inevitable battle, which soon came true with the assault on the Broken Shore.  It was due only to the naval expertise of Blackdawn's naval officers that the fleet 
				  survived, though their only undamaged ship that could still sail was the <em>Mercy</em>, the brain child of Reigan Ashford and Rosemary Dusklily.  As such, Blackdawn was once again limited to 
				  humanitarian efforts during the Legion Invasion, providing logistical and medical supply assistance to the war effort, with individual members of Blackdawn serving on the front lines in 
				  unofficial capacities.</p>
