				<h5 class="mb-0">
				  <a class="collapsed" data-toggle="collapse" data-parent="#accordion" href="#collapseTwo" aria-expanded="false" aria-controls="collapseTwo">Chapter Two: Gilnean Liberation Efforts</a>
				</h5>
			  </div>
			  <div id="collapseTwo" class="collapse" role="tabpanel" aria-labelledby="headingTwo">
				<div class="card-body">
				  <p>Prior to the Invasion of Gilneas and the Cataclysm of Azeroth, Blackdawn had essentially been split into two separate organizations.  One based inside of the Greymane Wall 
				  and lead by Ander Carmyne, and the other based in Stormwind under Alysdaire Blackdawn, who had pirated out of Gilneas for a number of reasons.  Alysdaire had posed as an 
				  expatriate Gilnean Nobleman and earned the patronage of Archmage Sirithil Nightstone, and Lord Kalren Geroux who was vassal to fellow Gilnean expatriate Baron Decimus Monroe.  He 
				  used these connections to further the influence of Blackdawn abroad, with intentions to create a second branch of Blackdawn that would provide a safe and stable home for 
				  all of Blackdawn in time to escape the growing Worgen Threat he had left behind.  Unbeknownst to Alysdaire, many of the original Blackdawns had already been infected, and were 
				  being rounded up by uninfected Blackdawn-led hunts to have them treated by Krennan Aranas.</p>
				  <p>Alysdaire's efforts came to particular fruition when he became aligned with Calithos Blyde of Silverpine, a former Scarlet Inquisitor with plans for an Alliance-lead 
				  reconquista of Lordaeron.  With Commander Taldrus Dawnfield and Justicar Arialynn Maewood leading a major Alliance force into Andorhal, Alysdaire's Blackdawns helped to provide 
				  reconnaisance and subterfuge services in exchange for a permanent base on Fenris Isle to fortify in the coming war.  Shortly after relocating to Fenris Isle, however, the Forsaken 
				  Invasion of Gilneas had begun, and soon the Cataclysm itself rocked the planet, devastating many areas.  As the Reconquista refocused to holding Andorhal and sending reinforcements 
				  to halt Kalimdor offensives by the Horde, Alysdaire remained in Silverpine to lend military assistance to the Gilnean Liberation Front.  Recommissioned as a Commander in the 
				  Gilnean Military due to his position and connections with the Alliance already, Alysdaire's Blackdawns assisted primarily in surgical strikes on Forsaken supply lines and 
				  evacuating civilians from Silverpine, Hillsbrad, and Gilneas.</p>
				  <p>Simultaneously, Ander's Blackdawns were fighting fiercely against the Forsaken Invasion, joining with Rebel and Royalist forces alike to stem the invasion and turn it back.  
				  Though a number of original Blackdawns were slain, Ander's team were able to ensure the cremation of many graves to prevent the dead from raising from Forsaken efforts, including 
				  assisting in surgical strikes in the Blackwald.  Ander specifically assisted in saving what he could from their headquarters in both Duskhaven before it sank and Stormglen before 
				  it was abandoned.  Ander's Blackdawns assisted in sabotaguing Forsaken machinery, and assisted evacuating many Gilneans to the Kaldorei boats, staying behind as a 
				  rearguard.  They joined up with Lord Darius Crowley's Gilnean Liberation Forces, and during the outward push they met up with Alysdaire's Blackdawns and were able to consolidate 
				  their numbers and set up an evacuation route to Fenris Isle, where mages assisted with teleportations around the clock to Stormwind.  Those that did not accept Lord Darius Crowley's 
				  call to arms and offering of the Worgen Curse via blood rituals were handed to Blackdawn's protection to evacuate safely.</p>
				  <p>Throughout the Cataclysm, Blackdawn's forces operated in Silverpine out of Fenris Ilse, providing reconnaisance, subterfuge, espionage, and sabotage against Forsaken forces 
				  in service to the Alliance, with many Blackdawns serving in both the Battle of Gilneas and Arathi Highlands.  Baron Decimus Monroe's forces, the Phantom Legion, marched to Dun Garok 
				  to establish a bulwark against further Forsaken incursions, and as such the two organizations often worked in conjunction with one another at first.  Eventually Alysdaire would find 
				  Monroe's methods of Total War against both military and civilian targets to go a touch far, including making monuments of dead men, women, and children from their assaults.  As such, 
				  Blackdawn would also have its Black Operations unit, then known as the Pathfinders, to disrupt the Phantom Legion's efforts, while Alysdaire himself took to the political battles in 
				  Stormwind and the Alliance Military (via his Gilnean military commission) to stymie Monroe's influence and attempts at further resources, funding, or authority.  This eventually came 
				  to a head when the Phantom Legion forcibly kidnapped an ally of Blackdawn from a Stormwind Guard prison to inact their own harsh punishment against her.  Aided by Taldrus Dawnfield, 
				  Simayi Wolfwood, Calithos Blyde, and many others, Alysdaire's forces joined others to essentially lay siege to The Phantom Legion's Alliance-owned barracks.  Eventually, Alysdaire's 
				  forces used Seaforium explosives to detonate the Phantom Legion armory, resulting in many injuries, the ruination of the keep, and forcing the Phantom Legion to retreat, leaving behind 
				  their prisoner.  From this point forward, Blackdawn was known as a force that put the well-being of their kin over the political allegiances to their fellow Gilneans, a trend that would 
				  earn them both respect and ire moving forward, and affect their relationships with many.</p>
