          <h2 id="historyTitle" class="my-4">Annals of Blackdawn</h2>
          <p>The History of Blackdawn is broken down into three primary chapters: The Founding Years, the Gilnean Liberation Efforts, the Humanitarian Philanthropy, and the Conflict and Struggle.  
		  To view any point of the Blackdawn Annals, simply click on the chapter name.  Chapters will be added, amended, or expanded as information becomes available, retconned, or rewritten for 
		  better narrative flow.</p>
		  <div class="mb-4" id="accordion" role="tablist" aria-multiselectable="true">
			<div class="card">
			  <div class="card-header" role="tab" id="headingOne">
				<h5 class="mb-0">
				  <a class="collapsed" data-toggle="collapse" data-parent="#accordion" href="#collapseOne" aria-expanded="false" aria-controls="collapseOne">Chapter One: The Founding Years</a>
				</h5>
			  </div>

			  <div id="collapseOne" class="collapse" role="tabpanel" aria-labelledby="headingOne">
				<div class="card-body">
				  <p>At the end of the Northgate Rebellion, Gilneas was in tatters - weak and wounded, the nation was now united under the Royal Banner once more, with many rebel leaders 
				  arrested, and many rebels forced to return to their lives living amongst their former foes.  Having fought on the side of the Royalists, Alysdaire's future was a bright 
				  one.  Tiberius was given a promotion and a handsome pension, and Alysdaire was offered to take up the mantle of his former instructors to assist in training replacement 
				  agents and officers lost during the rebellion.  On his way through the capital, Alysdaire was subjected to seeing the losses of the rebellion first hand: the homeless, 
				  the injured, and the orphaned.  Unable to face the people he was supposed to protect but had instead waged war against, Alysdaire took an early retirement and with the 
				  death of his father, used his inheritance to start a new organization - Blackdawn was Born, and it would protect those that were defenseless.</p>
				  <p>Joining Alysdaire would be his long-time friend and confidant, Ander Carmyne, who likewise wished to see the Royalist government care more for its citizenry than 
				  its nobles.  Also joining the group was Jaricho, a Gilnean mercenary they had befriended during the Rebellion who would act as the muscle for the fledgeling group.  
				  The trio set out to provide private security for more remote locations of Gilneas, particularly locations around the dangerous Blackwald.  In time others would join 
				  their organization, such as Tiberius's personal physician and alchemist Sershia Wolfthorn, the former Northgate Rebel 'Nyx' Forsythe, and Royalist sergeant and veteran 
				  Nicholas Vaelans.  Based out of Alysdaire's inherited manor in Duskhaven with a base of operation in Stormglen, the Blackdawn organization was small but well-liked by 
				  both rebel and royalist families.</p>
				  <p>Their work in the Blackwald soon started to increase in intensity, as murders started to occur both within the forest and along its borders.  Known as the Starlight 
				  Murders, Blackdawn was often hired by town mayors or small estates to protect themselves during the nights.  When Alysdaire's connections still remaining in the Gilnean 
				  Royal Services informed him of the long and secretive hunts the local nobility were performing in the Blackwald, hunting large beasts rather than spiders and foxes.  
				  During a few expeditions of their own, Blackdawn discovered these terrible beasts, and decided to capture one and determine what they were, where they came from, and 
				  to bring to light the darkness in the woods that the nobles seemed so keen to hide.</p>
				  <p>The stage was set - the entirety of Blackdawn would split in three parties to hunt and bag one of these beasts.  It was Alysdaire's group that found their quarry first, 
				  as such suffered the worst casualties in their attempt to capture the beast.  By the time reinforcements arrived, Alysdaire's group had been half-slain.  Jaricho, in his 
				  heavy armor, wrestled the beast before Ander struck the killing blow.  They then rushed the injured back to Stormglen, where Alysdaire was treated by Sershia with bandages 
				  and salves.  The group rested in the night and posted a guard on Alysdaire's room to ensure their injured leader would not be disturbed.</p>
				  <p>In the middle of the night screams were heard and soon enough a monster covered in bandages and blood burst from the building, attacking the guard and rousing 
				  the rest.  Given the bandages, attire, and injuries on the beast Ander and Jaricho were able to determine that the beast was in fact Alysdaire himself, and at great risk to 
				  themselves they captured and caged the leader.  Sershia then spent her entire collective efforts to determine the cause of the curse and attempt to return the man to his 
				  former self.  After months of testing with different doses and concoctions, the alchemist was able to subdue the beast and return Alysdaire's humanity, though the potions 
				  were far inferior to those that were later developed by Krennan Aranas and needed to be ingested constantly through the day.</p>
				  <p>Desperate for a cure and suffering crushing depression for the deaths caused at his own hands, Alysdaire enlisted Ander's assistance in pirating a ship out of Gilneas, 
				  bringing Sershia along with him in order to keep his bestial nature under control.  Ander was left in charge of Blackdawn, using their knowledge of the wolfbeast curse to 
				  prepare for the worst, and set up protection for their homes in Stormglen and Duskhaven.  Alysdaire posed as a Gilnean expatriate nobleman in Stormwind to gain influence 
				  and work towards securing assistance for his curse. The other Blackdawns were not so lucky, as the Worgen Curse had begun to spread and the Worgen were leading raids and invasions 
				  of Gilneas.  Soon a majority of the Blackdawns were infected, and in time with the assistance of Krennan Aranas and the royal alchemists, were returned to their humanity in 
				  far more complete manner than Alysdaire was.  While the Blackdawns had suffered in the absence of their leader, Ander had kept them intact with minimal, but still tragic, 
				  losses.  The worst was still yet to come, however...</p>
