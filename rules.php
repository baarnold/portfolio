				  <p>&lt;Blackdawn&gt;'s leadership is divided into two offices: The Guild Leader and the Officer Council.  All Officers are members of the 
				  Officer Council, and the Guild Leader is the premier member of the Officer Council.  The Guild Master has final say and approval on all 
				  guild events, designs, ideas, directions, goals, motions, and processes, but is expected to adhere to the demands of the Officer Council if 
				  they present a united position.  In this way, the Officer Council directly represents the members of the guild itself in voicing their concerns 
				  on guild activities or policies, and are directly responsible for ensuring good Guild Leadership by offering ideas, counsel, advise, and debate 
				  on any topic necessary.  OOCly, the Officer Council are trusted members of the guild who have accepted the responsibility of leadership, whether 
				  by being trusted by the Guild Leader and accepted by the members, or trusted by the members and accepted by the Guild Leader.  ICly, the Officer 
				  Council represents the leaders of certain divisions of the guild, such as military, civilian, or merchant divisions.</p>
				  <p>If there are any complaints about an officer, please report it to another officer you trust or, in lieu of this, the Guild Leader themself. 
				  This includes situations where they make you feel uncomfortable or appears to be violating the rules of the guild for any reason.  If you have 
				  issue with the Guild Leader themselves, you may approach all Officers of the Officer Council to speak to them on your behalf if you are uncomfortable 
				  speaking directly to the Guild Leader.  &lt;Blackdawn&gt; strives for transparency and accountability, but we won't always succeed; let us know 
				  if mistakes are made and we will do our best to rectify them in the future.</p>
				  <p><strong>NCOs:</strong> For lack of a better term, our NCOs are trusted members of the guild who are given particular oversight of certain duties 
				  or tasks that grant some modicum of authority over other members and in some cases the officers.  Examples would be a Raid Leader, who may be granted 
				  authority to organize and run Raids without officer oversight and would in fact be allowed to order officers around (within reason) during the raids.
				  NCOs will be granted appropriate in-game titles to differentiate themselves in the Guild Roster, and will be listed in any rosters alongside the 
				  Guild Officers.</p>
				  <p><strong>Alts:</strong> Alternate characters are not allowed to hold ranks in the guild.  This includes the Guild Officers.  While alts may be 
				  granted Roleplay ranks, they will never be granted ranks outside of NCO or Alt.  This is to maintain an accurate roster of active officers and 
				  members in the guild.  If you wish to make an Alt be your new main character, please speak with an Officer.  If an Officer wishes to make a new main 
				  character, please see the Guild Leader and we will arrange for the IC and OOC replacement as needed.</p>
